const clockface = document.querySelector(".js-time");
const startBtn = document.querySelector(".js-start");
const resetBtn = document.querySelector(".js-reset");


const timer = {
  startTime: null,
  deltaTime: null,
  pauseTime: null,
  id: null,
  isActive: false,
  start(){
      if(this.isActive) return;
      this.isActive = true;
   this.startTime = Date.now();
   this.id = setInterval(() => {
       const currentTime = Date.now();
       this.deltaTime = currentTime - this.startTime;
       updateClockface(clockface, this.deltaTime);
   }, 100);
  },
  reset(){
   this.deltaTime = 0;
   updateClockface(clockface, this.deltaTime)
   startBtn.textContent = 'Start';
   clearInterval(this.id);
    this.isActive = false;
  },
  pause(){
      if(timer.isActive){
        clearInterval(this.id);
        this.isActive = false;
      } else{
        this.id = setInterval(() => {
            const currentTime = Date.now();
            this.pauseTime = currentTime - this.startTime - this.deltaTime;
            updateClockface(clockface, this.pauseTime);
        }, 100);
      }
  },
};

startBtn.addEventListener('click', handleStartBtnClick);
function handleStartBtnClick (evt) {
    if(timer.isActive){
        timer.start();
        startBtn.textContent = 'Pause';
    }else {
        timer.pause();
   startBtn.textContent = 'Continue';
}
}

resetBtn.addEventListener('click', timer.reset.bind(timer));

